# За дато n исписати чланове низа 1, 2, 2, 3, 3, 3, 4, 4, 4, 4, . . . , n, . . . , n
# (низ садржи једну јединицу, две двојке,
# три тројке итд.).
#
# Улаз: Са стандардног улаза се учитава природан број n (1 ≤ n ≤ 30).
#
# Излаз: Чланове траженог низа исписати на стандардни излаз (иза сваког исписати један размак).
#
# Пример
# Улаз
# 4
# Излаз
# 1 2 2 3 3 3 4 4 4 4

n= int( input("Unesi broj: "))

for i in range(1,n+1):
    for j in range(0,i):
        print(i," ",sep="",end="")
